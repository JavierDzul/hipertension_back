from typing import Any


CHOLESTEROL_LEVELS = [116, 154, 193, 232, 270]
SYSTOLIC_LEVELS = [180, 160, 140, 120]


def make_risk_cell(value: int | str) -> dict[str, Any]:
    if value == "<1":
        return {
            "risk_display": "<1%",
            "risk_percent": 0.5,
            "is_less_than_one": True,
            "risk_category": "<1%",
        }

    risk_percent = int(value)

    if risk_percent == 1:
        risk_category = "1%"
    elif risk_percent == 2:
        risk_category = "2%"
    elif 3 <= risk_percent <= 4:
        risk_category = "3-4%"
    elif 5 <= risk_percent <= 9:
        risk_category = "5-9%"
    elif 10 <= risk_percent <= 14:
        risk_category = "10-14%"
    else:
        risk_category = "≥15%"

    return {
        "risk_display": f"{risk_percent}%",
        "risk_percent": risk_percent,
        "is_less_than_one": False,
        "risk_category": risk_category,
    }


def build_age_table(rows_by_age: dict[int, list[list[int | str]]]) -> dict[int, dict[int, dict[int, dict[str, Any]]]]:
    table = {}

    for age, systolic_rows in rows_by_age.items():
        table[age] = {}

        for systolic_index, systolic in enumerate(SYSTOLIC_LEVELS):
            table[age][systolic] = {}

            for cholesterol_index, cholesterol in enumerate(CHOLESTEROL_LEVELS):
                value = systolic_rows[systolic_index][cholesterol_index]
                table[age][systolic][cholesterol] = make_risk_cell(value)

    return table


